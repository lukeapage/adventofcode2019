const findMeteriods = require('./findmeteriods');

test('example 1', () => {
    expect(
        findMeteriods(`
.#..#
.....
#####
....#
...##`)
    ).toMatchInlineSnapshot(`
        Object {
          "visible": 8,
          "x": 3,
          "y": 4,
        }
    `);
});

